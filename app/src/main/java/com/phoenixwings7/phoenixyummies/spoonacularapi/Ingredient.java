package com.phoenixwings7.phoenixyummies.spoonacularapi;

public class Ingredient {
    private long id;
    private String name;
    private String unit;
    private String unitShort;

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getUnit() {
        return unit;
    }

    public String getUnitShort() {
        return unitShort;
    }
}
